
# list
# menggunakan kurung siku []
data_list = [10, 2, 4, 6, 3, 1]
print(data_list)

# tupels
# menggunakan kurung biasa ()
data_tuples = (9, 39, 3, 5, 1)  # isi tipe data tuples tidak bisa diubah
print(data_tuples)
# tuples digunakan untuk menyajikan data yang tidak boleh diubah

# sets
# menggunakan kurung kurawal {}
data_sets = {10, 35, 5, 6, 2, 7}    # isi tipe data set tidak beraturan saat ditampilkan
print(data_sets)
# data sets tidak bisa menggunakan indexing, namun tetap bisa menggunakan method seperti append dll
