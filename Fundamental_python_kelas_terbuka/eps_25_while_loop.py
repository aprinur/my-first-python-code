"""
struktur while loop:

while kondisi:
    aksi ini
    aksi itu

akhir dari program
"""

angka = 0
print(f'angka sekarang --> {angka}')

while angka < 5:
    print(f"angka sekarang --> {angka}")
    print('otong ganteng maxsyimall!!!')
    angka += 1
'''
angka awal adalah 0, karena dalam kondisi yang diberikan yaitu angka kurang dari 5 maka
kondisi tersebut true, jadi perulangan akan dilanjutkan ke baris berikutnya yaitu 
angka += 1 dan diteruskan ke baris berikutnya lalu diulang 
sampai angka tidak lagi kurang dari 5 atau false
 '''
