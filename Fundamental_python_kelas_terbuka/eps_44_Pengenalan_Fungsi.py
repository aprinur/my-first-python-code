# Fungsi
"""
    Fungsi atau function disebut juga method atau rutin atau subrutin.
    Cara mendefinisikan fungsi dengan def lalu diikuti nama_global fungsi.
    Fungsi dibuat agar tidak mengulang ulang program yang sama.
    Badan fungsi bisa berisi apa saja.
    template fungsi adalah:

    def nama_fungsi(argumen/parameter):
        badan fungsi
"""


# Contoh pendefinisian fungsi
def hello_world():  # pendefinisian fungsi
    """fungsi menampilkan hello world"""
    print('hello world')


'''Untuk menggunakan fungsi cukup memanggil nama_global fungsi '''
hello_world()   # pemanggilan fungsi


def fungsi():
    print('Ini adalah fungsi')


'''pemanggilan fungsi harus dilakukan setelah fungsi didefinisikan/dibuat'''
fungsi()     # pemanggilan fungsi